    public void CreateNewEvent(int tStart, int tStop, Color color1, string eventName)
    {
        EventObject = GameObject.Find("EventObject");
        GameObject event1 = Instantiate(EventObject);
        TextObject = GameObject.Find("TextObject");
        GameObject text1 = Instantiate(TextObject);
        Renderer rend = event1.GetComponent<Renderer>();
        var tempMaterial = new Material(rend.sharedMaterial);
        tempMaterial.color = color1;
        float emission = Mathf.PingPong(Time.time, 1.0f);
        Color finalColor = color1 * Mathf.LinearToGammaSpace(emission);
        tempMaterial.SetColor("_EmissionColor", finalColor);
        rend.sharedMaterial = tempMaterial;
        event1.GetComponent<ShortTermEvent>().start = tStart;
        event1.GetComponent<ShortTermEvent>().stop = tStop;
        text1.GetComponent<TextFollowObject>().start = tStart;
        text1.GetComponent<TextFollowObject>().stop = tStop;

        TextMesh text2 = text1.GetComponent<TextMesh>();
        text2.text = eventName;
        text2.color = color1;
    }
